    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String fournisseurs = txtFournisseurs.getText();
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        Date dateF = dateFab.getDate();
        Date dateE = dateExp.getDate();
        String dateFabrication = "";
        String dateExpiration = "";
        if (dateF != null) {
            dateFabrication = df.format(dateFab.getDate());
        }
        if (dateE != null) {
            dateExpiration = df.format(dateExp.getDate());
        }
        String id = txtID.getText();
        String nom = txtNom.getText();
        int qte = Integer.parseInt(txtQuantite.getText());

        int prix = Integer.parseInt(txtPrix.getText());

        if (nom.equals("")) {
            JOptionPane.showMessageDialog(null, "Le champ Nom est vide");
        } else if (fournisseurs.equals("")) {
            JOptionPane.showMessageDialog(null, "Le champ Fournisseur est vide");
        } else if (qte == 0) {
            JOptionPane.showMessageDialog(null, "Le champ Quantite est vide");
        } else if (prix == 0) {
            JOptionPane.showMessageDialog(null, "Le champ Prix est vide");
        } else if (dateFabrication.equals("")) {
            JOptionPane.showMessageDialog(null, "Le champ date de fabrication est vide");
        } else if (dateExpiration.equals("")) {
            JOptionPane.showMessageDialog(null, "Le champ date d'Expriration est vide");
        } else {
            try {
                con = Connect.getConnection();
                pst = con.prepareStatement("INSERT INTO medicaments (nom,quantite,prix,fournisseurs,fabircation,expiration) values(?,?,?,?,?,?)");
                pst.setString(1, nom);
                pst.setInt(2, qte);
                pst.setInt(3, prix);
                pst.setString(4, fournisseurs);
                pst.setString(5, dateFabrication);
                pst.setString(6, dateExpiration);
                res = pst.executeQuery();
                JOptionPane.showConfirmDialog(null, "Voulez vous continuer?", "Message", JOptionPane.YES_NO_OPTION);

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Le médicament a bien été inséré avec succès");
            }
        }
        setVisible(false);
        new Medicaments().setVisible(true);

    }//GEN-LAST:event_jButton1ActionPerformed

