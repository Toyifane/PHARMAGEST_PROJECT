    private void tbMedocMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMedocMouseClicked
        // TODO add your handling code here:
        int row = tbMedoc.getSelectedRow();
        String t = tbMedoc.getModel().getValueAt(row, 0).toString();
        String sql = "select * from medicaments where idmed='" + t + "' ";
        try {
            pst = con.prepareStatement(sql);
            res = pst.executeQuery();
            if (res.next()) {
                String id = res.getString("idmed");
                txtID.setText(id);
                String nom = res.getString("nom");
                txtNom.setText(nom);
                String quantite = res.getString("quantite");
                txtQuantite.setText(quantite);
                String prix = res.getString("prix");
                txtPrix.setText(prix);
                String fournisseur = res.getString("fournisseurs");
                txtFournisseurs.setText(fournisseur);
                String dateF = res.getString("fabircation");
                dateFab.setDateFormatString(dateF);
                String dateE = res.getString("expiration");
                dateExp.setDateFormatString(dateE);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Bonnes informations", "Erreur", 2);

        }
    }//GEN-LAST:event_tbMedocMouseClicked

