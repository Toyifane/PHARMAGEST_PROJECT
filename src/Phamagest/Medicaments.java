    private boolean verifierm() {
        boolean verifier = false;
        String sql = "select idmed from medicaments where idmed='" + txtID.getText() + "' ";
        try {
            pst = con.prepareStatement(sql);
            res = pst.executeQuery();
            if (res.next()) {
                verifier = true;
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Erreur", 2);
        }
        return verifier;

    }

    public void incrementid() {
        try {
            String sql = "SELECT idmed FROM medicaments order by idmed desc limit 1";
            pst = con.prepareStatement(sql);
            res = pst.executeQuery();
            if (res.next()) {
                int id = res.getInt(1);
                int n = id + 1;
                txtID.setText(Integer.toString(n));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Erreur", 2);
        }
    }

